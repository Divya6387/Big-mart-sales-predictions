import os
import warnings
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.model_selection import KFold, cross_val_score
from sklearn.preprocessing import LabelEncoder
from sklearn.linear_model import Ridge
from sklearn.metrics import mean_squared_error

import xgboost as xgb
import lightgbm as lgb

warnings.filterwarnings("ignore")
np.random.seed(42)

TRAIN_PATH = "train_v9rqX0R.csv"
TEST_PATH  = "test_AbJTz2l.csv"
OUTPUT_DIR = "outputs"
os.makedirs(OUTPUT_DIR, exist_ok=True)

#1. Load data
print("=" * 60)
print("STEP 1 – Loading data")
print("=" * 60)

train = pd.read_csv(TRAIN_PATH)
test  = pd.read_csv(TEST_PATH)

print(f"Train shape : {train.shape}")
print(f"Test  shape : {test.shape}")

# Keep test IDs for submission
test_ids = test[["Item_Identifier", "Outlet_Identifier"]].copy()

#2. Quick EDA
print("\n" + "=" * 60)
print("STEP 2 – Exploratory Data Analysis")
print("=" * 60)

print("\nTarget (Item_Outlet_Sales) stats:")
print(train["Item_Outlet_Sales"].describe())

print("\nMissing values – Train:")
print(train.isnull().sum()[train.isnull().sum() > 0])
print("\nMissing values – Test:")
print(test.isnull().sum()[test.isnull().sum() > 0])

# Save EDA plots
fig, axes = plt.subplots(1, 3, figsize=(18, 5))
train["Item_Outlet_Sales"].hist(bins=50, ax=axes[0], color="#4C72B0")
axes[0].set_title("Target Distribution (raw)")
axes[0].set_xlabel("Item_Outlet_Sales")

np.log1p(train["Item_Outlet_Sales"]).hist(bins=50, ax=axes[1], color="#55A868")
axes[1].set_title("Target Distribution (log1p)")
axes[1].set_xlabel("log1p(Item_Outlet_Sales)")

train.groupby("Outlet_Type")["Item_Outlet_Sales"].mean().plot(
    kind="bar", ax=axes[2], color="#C44E52", rot=15
)
axes[2].set_title("Mean Sales by Outlet Type")
axes[2].set_ylabel("Mean Sales")

plt.tight_layout()
plt.savefig(os.path.join(OUTPUT_DIR, "eda_plots.png"), dpi=120)
plt.close()
print(f"\n[Saved] {OUTPUT_DIR}/eda_plots.png")

#3. Combine train + test for joint preprocessing
print("\n" + "=" * 60)
print("STEP 3 – Data Cleaning & Imputation")
print("=" * 60)

train["source"] = "train"
test["source"]  = "test"
test["Item_Outlet_Sales"] = np.nan

data = pd.concat([train, test], ignore_index=True, sort=False)

# 3a. Standardise Item_Fat_Content
fat_map = {"LF": "Low Fat", "low fat": "Low Fat",
           "reg": "Regular", "Regular": "Regular", "Low Fat": "Low Fat"}
data["Item_Fat_Content"] = data["Item_Fat_Content"].map(fat_map)

# Non-consumable items should have "Non-Edible" fat content
non_food = ["Health and Hygiene", "Household", "Others"]
data.loc[data["Item_Type"].isin(non_food), "Item_Fat_Content"] = "Non-Edible"

# 3b. Impute Item_Weight with per-item mean (stable across stores)
item_weight_mean = (
    data.groupby("Item_Identifier")["Item_Weight"].mean()
)
data["Item_Weight"] = data.apply(
    lambda row: item_weight_mean[row["Item_Identifier"]]
    if pd.isnull(row["Item_Weight"]) else row["Item_Weight"],
    axis=1,
)

# Any residual → global mean
data["Item_Weight"].fillna(data["Item_Weight"].mean(), inplace=True)

# 3c. Impute Outlet_Size with mode per Outlet_Type
outlet_size_mode = (
    data.groupby("Outlet_Type")["Outlet_Size"]
    .agg(lambda x: x.mode()[0] if not x.mode().empty else "Small")
)
data["Outlet_Size"] = data.apply(
    lambda row: outlet_size_mode[row["Outlet_Type"]]
    if pd.isnull(row["Outlet_Size"]) else row["Outlet_Size"],
    axis=1,
)

# 3d. Fix Item_Visibility zeros (physically impossible)
data["Item_Visibility"] = data["Item_Visibility"].replace(0, np.nan)
vis_mean = data.groupby("Item_Identifier")["Item_Visibility"].mean()
data["Item_Visibility"] = data.apply(
    lambda row: vis_mean[row["Item_Identifier"]]
    if pd.isnull(row["Item_Visibility"]) else row["Item_Visibility"],
    axis=1,
)

print("Missing values after imputation:", data.isnull().sum()[data.isnull().sum() > 0].to_dict())

#4. Feature Engineering
print("\n" + "=" * 60)
print("STEP 4 – Feature Engineering")
print("=" * 60)

# 4a. Outlet age
data["Outlet_Age"] = 2013 - data["Outlet_Establishment_Year"]

# 4b. Item category from first 2 chars of Item_Identifier
data["Item_Category"] = data["Item_Identifier"].str[:2]

# 4c. Visibility ratio relative to item's mean visibility
item_vis_mean = data.groupby("Item_Identifier")["Item_Visibility"].mean()
data["Visibility_Ratio"] = data.apply(
    lambda row: row["Item_Visibility"] / item_vis_mean[row["Item_Identifier"]],
    axis=1,
)

# 4d. MRP buckets
data["MRP_Bucket"] = pd.cut(
    data["Item_MRP"],
    bins=[0, 70, 130, 200, 270],
    labels=["Low", "Medium", "High", "Very_High"],
)

# 4e. Indicator: is Grocery Store?
data["Is_Grocery"] = (data["Outlet_Type"] == "Grocery Store").astype(int)

print("New features added: Outlet_Age, Item_Category, Visibility_Ratio, MRP_Bucket, Is_Grocery")
print("Feature count:", data.shape[1])

#5. Encoding
print("\n" + "=" * 60)
print("STEP 5 – Label Encoding")
print("=" * 60)

cat_cols = [
    "Item_Fat_Content", "Item_Type", "Outlet_Identifier",
    "Outlet_Size", "Outlet_Location_Type", "Outlet_Type",
    "Item_Category", "MRP_Bucket",
]

le = LabelEncoder()
for col in cat_cols:
    data[col] = data[col].astype(str)
    data[col] = le.fit_transform(data[col])
    print(f"  Encoded: {col}")

# 6. Prepare train / test splits
DROP_COLS = ["Item_Identifier", "Outlet_Establishment_Year", "source"]

train_clean = data[data["source"] == 0].drop(columns=DROP_COLS)  # source was encoded
# Better: split on original flag before encoding
train_clean = data[data["Item_Outlet_Sales"].notna()].drop(columns=DROP_COLS)
test_clean  = data[data["Item_Outlet_Sales"].isna()].drop(columns=DROP_COLS + ["Item_Outlet_Sales"])

FEATURE_COLS = [c for c in train_clean.columns if c != "Item_Outlet_Sales"]
X = train_clean[FEATURE_COLS]
y = np.log1p(train_clean["Item_Outlet_Sales"])   # log-transform target
X_test = test_clean[FEATURE_COLS]

print(f"\nX shape: {X.shape},  X_test shape: {X_test.shape}")

#7. Model Training
print("\n" + "=" * 60)
print("STEP 6 – Model Training (5-Fold CV)")
print("=" * 60)

kf = KFold(n_splits=5, shuffle=True, random_state=42)

#  XGBoost
xgb_params = dict(
    n_estimators=800,
    learning_rate=0.05,
    max_depth=6,
    subsample=0.8,
    colsample_bytree=0.8,
    reg_alpha=0.1,
    reg_lambda=1.0,
    random_state=42,
    n_jobs=-1,
    verbosity=0,
)
xgb_model = xgb.XGBRegressor(**xgb_params)
xgb_cv = cross_val_score(
    xgb_model, X, y, cv=kf, scoring="neg_root_mean_squared_error"
)
print(f"XGBoost CV RMSE (log-space): {-xgb_cv.mean():.4f} ± {xgb_cv.std():.4f}")

# LightGBM
lgb_params = dict(
    n_estimators=800,
    learning_rate=0.05,
    max_depth=6,
    num_leaves=50,
    subsample=0.8,
    colsample_bytree=0.8,
    reg_alpha=0.1,
    reg_lambda=1.0,
    random_state=42,
    n_jobs=-1,
    verbose=-1,
)
lgb_model = lgb.LGBMRegressor(**lgb_params)
lgb_cv = cross_val_score(
    lgb_model, X, y, cv=kf, scoring="neg_root_mean_squared_error"
)
print(f"LightGBM CV RMSE (log-space): {-lgb_cv.mean():.4f} ± {lgb_cv.std():.4f}")

#  Ridge
ridge_model = Ridge(alpha=10)
ridge_cv = cross_val_score(
    ridge_model, X, y, cv=kf, scoring="neg_root_mean_squared_error"
)
print(f"Ridge    CV RMSE (log-space): {-ridge_cv.mean():.4f} ± {ridge_cv.std():.4f}")

# Fit full models
print("\nFitting full models on entire training data...")
xgb_model.fit(X, y)
lgb_model.fit(X, y)
ridge_model.fit(X, y)

#  Ensemble (weighted average)
xgb_pred   = np.expm1(xgb_model.predict(X_test))
lgb_pred   = np.expm1(lgb_model.predict(X_test))
ridge_pred = np.expm1(ridge_model.predict(X_test))

# Weights inversely proportional to CV RMSE
w_xgb, w_lgb, w_ridge = 0.45, 0.45, 0.10
final_pred = w_xgb * xgb_pred + w_lgb * lgb_pred + w_ridge * ridge_pred

# 8. Feature Importance Plot
feat_imp = pd.DataFrame({
    "feature": FEATURE_COLS,
    "importance": xgb_model.feature_importances_,
}).sort_values("importance", ascending=False)

plt.figure(figsize=(10, 6))
sns.barplot(x="importance", y="feature", data=feat_imp.head(15), palette="viridis")
plt.title("Top-15 Feature Importances (XGBoost)")
plt.tight_layout()
plt.savefig(os.path.join(OUTPUT_DIR, "feature_importance.png"), dpi=120)
plt.close()
print(f"\n[Saved] {OUTPUT_DIR}/feature_importance.png")

# 9. Submission File
print("\n" + "=" * 60)
print("STEP 7 – Creating Submission File")
print("=" * 60)

submission = test_ids.copy()
submission["Item_Outlet_Sales"] = final_pred

submission_path = os.path.join(OUTPUT_DIR, "submission.csv")
submission.to_csv(submission_path, index=False)
print(f"[Saved] {submission_path}")
print(f"\nSubmission preview:\n{submission.head()}")
print(f"\nPrediction stats:\n{submission['Item_Outlet_Sales'].describe()}")
print("\n Done!")





